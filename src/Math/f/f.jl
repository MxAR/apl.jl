    ##-----------------------------------------------------------------------------------
    function samples{T<:Any}(data::Array{T, 1}, size::Integer)
        L = length(data)
        @assert(size < L, "The number of samples musn't be bigger than the data!")
        return shuffle(getindex(data, sort(sample(1:L, size, replace = false))))
    end


